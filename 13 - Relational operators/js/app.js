"use strict"

//  Relational operators

var dateA = 10;
var dateB = 20;

// Greater than >
var exa1 = dateA > dateB;
console.log("Is " + dateA + "greater than " + dateB + ":", exa1);

// Smaller than <
var exa2 = dateA < dateB;
console.log("Is " + dateA + "smaller than " + dateB + ":", exa2);

// Is greater than or equal to >=
var exa3 = dateA >= dateB;
console.log("Is " + dateA + "greater or equal to " + dateB + ":", exa3);

// Is smaller than or equal to <=
var exa4 = dateA <= dateB;
console.log("Is " + dateA + "smaller or equal to " + dateB + ":", exa4);

// Is equal to ==
var exa5 = dateA == dateB;
console.log("Is " + dateA + "equal to " + dateB + ":", exa5);

// Is equal to !=
var exa6 = dateA != dateB;
console.log("Is " + dateA + " different or equal to " + dateB + ":", exa6);