"use strict"

//  Aritmetic operators

var dateA = 10;
var dateB = 20;

// SUM +
var sum = dateA + dateB;
console.log("The sum of " + dateA + " + " + dateB + " is :", sum);

// SUBTRACTION -
var subt = dateA - dateB;
console.log("The substraction of " + dateA + " - " + dateB + " is :", subt);

// MULTIPLICATION * 
var multiplication = dateA * dateB;
console.log("The multiplication of " + dateA + " * " + dateB + " is :", multiplication);

// DIVISION /
var division = dateA / dateB;
console.log("The division of " + dateA + " / " + dateB + " is :", division);

// RESIDUE %
var residue = dateA % dateB;
console.log("The residue of " + dateA + " % " + dateB + " is :", residue);

// INCREMENT ++
var increment = dateA;
increment++; // --> === increment+1
console.log("The increment of " + dateA + " is :", increment);

// DECREMENT --
var decrement = dateA;
decrement--; // --> === increment-1
console.log("The decrement of " + dateA + " is :", decrement);



