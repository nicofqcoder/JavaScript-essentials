"use strict"

// Assignment operators

var dateA = 10;

// Simple assignment
var equal = dateA;
console.log("The result of the simple assignment is: "+ equal);

// Sum and assignment
var sumEqual = 10;
sumEqual += dateA; // === sumEqual + dateA
console.log("The result of the assignment is: "+ sumEqual);

// Substract and assignment
var subsEqual = 10;
subsEqual -= dateA; // === subsEqual - dateA
console.log("The result of the assignment is: "+ subsEqual);

// Multiply and assignment
var mulEqual = 10;
mulEqual *= dateA; // === mulEqual * dateA
console.log("The result of the assignment is: "+ mulEqual);

// Division and assignment
var divEqual = 20;
divEqual /= dateA; // === divEqual / dateA
console.log("The result of the assignment is: "+ divEqual);

